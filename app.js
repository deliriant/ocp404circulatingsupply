// Initialize contract instances
const originalTokenContract = new web3.eth.Contract(originalTokenAbi, originalTokenAddress);
const lpTokenContract = new web3.eth.Contract(lpTokenAbi, lpTokenAddress);

app.use(cors());

app.get('/api/totalSupply', async (req, res) => {
  try {
    const totalSupply = await originalTokenContract.methods.totalSupply().call();
    const decimals = await originalTokenContract.methods.decimals().call();
    const formattedTotalSupply = totalSupply / Math.pow(10, decimals);
    res.json({ totalSupply: formattedTotalSupply.toString() });
  } catch (err) {
    console.error('Error executing query', err.stack);
    res.status(500).send('Server Error');
  }
});

app.get('/api/circulatingSupply', async (req, res) => {
  try {
    const totalSupply = await originalTokenContract.methods.totalSupply().call();
    const lpBalance = await originalTokenContract.methods.balanceOf(lpTokenAddress).call();
    const decimals = await originalTokenContract.methods.decimals().call();

    const circulatingSupply = (totalSupply - lpBalance) / Math.pow(10, decimals);
    res.json({ circulatingSupply: circulatingSupply.toString() });
  } catch (err) {
    console.error('Error fetching circulating supply', err.stack);
    res.status(500).send('Server Error');
  }
});

const port = process.env.PORT || 3000;
app.listen(port, () => {
  console.log(`Server running on port ${port}`);
});
